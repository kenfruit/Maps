		static void Main(string[] args)
		{

			BuildRestaurantList();

			String outputFileName = MakeDatedFileString("yelpScanner.txt");
			String outputFileNameHours = MakeDatedFileString("yelpScannerHours.txt");


			int listCount = restaurantList.Count;
			int currentItem = 0;
			using (FileStream fs = File.Create(outputFileName),
							  fsh = File.Create(outputFileNameHours))
			{
				string text = string.Empty;

				foreach (string url in restaurantList)
				{
				   currentItem++;
					Trace.WriteLine("To process:" + listCount.ToString() +
									"  Current: " + currentItem.ToString() +
									"   " + DateTime.Now.ToLocalTime());

					text = string.Empty;
					text += url + "\t";
					HtmlAgilityPack.HtmlDocument doc = null;
					try
					{
						var Webget = new HtmlWeb();
						doc = Webget.Load(url);
					}
					catch
					{
						Thread.Sleep(1 * 60 * 1000);
						fs.Write(new UTF8Encoding(true).GetBytes(text), 0, text.Length);
						continue;
					}
					string title = doc.DocumentNode.SelectNodes("//title")[0].InnerText;

					if (title.Contains("404") ||
						title.Contains("- CLOSED"))
					{
						text += title + "\r\n";
					}
					else
					{
						// Parse the Hours table
						try
						{
							bool servesBreakfast = false;
							var hourTable = doc.DocumentNode.SelectNodes("//table[@class='table table-simple hours-table']//tr");
							//fs.Write(new UTF8Encoding(true).GetBytes(text), 0, text.Length);


							if (hourTable == null)
							{
								text += "No hours listed in Yelp";
							}
							else
							{
								foreach (HtmlNode node in hourTable)
								{
									foreach (HtmlNode node3 in node.SelectNodes("th"))
									{
										text += node3.InnerText;
										text += " ";
									}

									bool isFirst = true;
									if (0 < node.InnerText.IndexOf("Closed") &&
										!(0 < node.InnerText.IndexOf("Closed now")))
									{
										text += "Closed" + "&lt;br/&gt;";
										continue;
									}
                                    if (0 < node.InnerText.IndexOf("Open 24 hours"))
                                    {
                                        text += "Open 24 hours" + "<br/>";
                                        continue;

                                    }

									int segmentCount = 1;
									foreach (HtmlNode node4 in node.SelectNodes(".//span"))
									{
										if (0 == segmentCount % 2)
										{
											text += "- ";
										}
										string innerText = node4.InnerText;
										int testInt;
										bool isNumeric = int.TryParse(innerText.Substring(0, 1), out testInt);
										if (!isNumeric)
										{
											break;
										}
										if (innerText.Contains("am") && isFirst)
										{
											if (int.TryParse(innerText.Substring(0, 1), out testInt) &&
												 1 < testInt)
											{
												servesBreakfast = true;
											}
										}
										text += innerText + " ";
										isFirst = false;
										segmentCount++;
									}

									text += "&lt;br/&gt;";

								}
								if (servesBreakfast)
								{
									text += "\tBREAKFAST";
								}
							}
						}
						catch (Exception e)
						{
							string s = e.Message;
							text += s;
						}
					}
					text += "\r\n";
					//Console.WriteLine(text);

					fs.Write(new UTF8Encoding(true).GetBytes(text), 0, text.Length);
					Thread.Sleep(1 * 60 * 1000);
				} // end foreach


			} // end using
		}

		static String MakeDatedFileString(String inString)
		{
			String outString = inString;

			DateTime now = DateTime.Now;

			String formattedDate = "_" + now.ToString("M d yy h mm ss") + ".";
			formattedDate = formattedDate.Replace(' ', '_');

			return outString.Replace(".", formattedDate);

		}

